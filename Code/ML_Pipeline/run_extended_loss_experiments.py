if __name__ == "__main__":
    print_section("EXTENDED LOSS FUNCTION EXPERIMENT", 
                  "MAE loss for LSTM/GRU/Transformer across all baseline horizons")
    
    # Load experiments to run
    if not os.path.exists(EXPERIMENTS_CSV):
        print(f"\nERROR: {EXPERIMENTS_CSV} not found.")
        print(f"Please run check_nn_experiments_per_horizon.py first to generate it.")
        sys.exit(1)
    
    print(f"\nLoading experiment list from: {EXPERIMENTS_CSV}")
    df_experiments = pd.read_csv(EXPERIMENTS_CSV)
    total_experiments = len(df_experiments)
    print(f"  Loaded {total_experiments} experiments")
    
    # Check for already-completed experiments
    if SKIP_EXISTING:
        print(f"\nChecking for existing MAE loss experiments...")
        existing = load_existing_mae_loss_experiments()
        print(f"  Found {len(existing)} already completed")
        
        if existing:
            mask = df_experiments.apply(
                lambda row: (row['Model'], row['Target'], row['Horizon'], row['Experiment']) not in existing,
                axis=1
            )
            df_experiments = df_experiments[mask].reset_index(drop=True)
            print(f"  Skipping {total_experiments - len(df_experiments)} already-done experiments")
            print(f"  Remaining to run: {len(df_experiments)}")
    
    if df_experiments.empty:
        print("\n  All experiments already complete!")
        sys.exit(0)
    
    # Disable pruning engine
    config.USE_PRUNING_ENGINE = False
    
    # Apply MAE loss patches
    print(f"\nPatching NN wrappers to use MAE loss...")
    orig_rnn_fit, orig_tfm_fit = patch_nn_wrappers_for_mae_loss()
    
    print(f"\nStarting {len(df_experiments)} experiments...")
    print(f"  Models in queue: {sorted(df_experiments['Model'].unique())}")
    print(f"  Targets: {sorted(df_experiments['Target'].unique())}")
    print(f"  Horizons: {sorted(df_experiments['Horizon'].unique())}")
    
    # Group by model so we only call enable_single_model() once per model
    df_experiments = df_experiments.sort_values(['Model', 'Target', 'Horizon', 'Experiment']).reset_index(drop=True)
    
    completed = 0
    failed = 0
    current_model = None
    total_to_run = len(df_experiments)
    
    overall_start = time.time()
    
    try:
        for idx, row in df_experiments.iterrows():
            model_name = row['Model']
            target = row['Target']
            horizon = int(row['Horizon'])
            base_exp = row['Experiment']
            
            # Lookup feature groups
            groups = EXP_GROUPS.get(base_exp)
            if groups is None:
                print(f"\n  [SKIP] Unknown experiment: {base_exp}")
                failed += 1
                continue
            
            # Switch model if needed
            if model_name != current_model:
                print_section(f"MODEL: {model_name}")
                enable_single_model(model_name)
                current_model = model_name
            
            completed += 1
            elapsed = (time.time() - overall_start) / 60
            avg_per_exp = elapsed / completed if completed > 0 else 0
            eta_min = (total_to_run - completed) * avg_per_exp
            
            print(f"\n[{completed}/{total_to_run}] {model_name} | {target} | {horizon}h | {base_exp}")
            print(f"  Elapsed: {elapsed:.1f} min | ETA: {eta_min:.1f} min")
            
            # Experiment name follows the existing convention:
            # {feature_set}_{horizon}h_{target}_MAELoss
            exp_name = f"{base_exp}_{horizon}h_{target}_MAELoss"
            
            success = run_experiment(
                name    = exp_name,
                groups  = groups,
                region  = "DK1",
                horizon = horizon,
                target  = target
            )
            
            if not success:
                failed += 1
    
    finally:
        # Always restore original methods
        print("\nRestoring original NN fit methods...")
        restore_nn_wrappers(orig_rnn_fit, orig_tfm_fit)
    
    # Summary
    total_time = (time.time() - overall_start) / 60
    print_section("EXPERIMENT COMPLETE")
    print(f"  Completed: {completed - failed}/{total_to_run}")
    if failed > 0:
        print(f"  Failed:    {failed}")
    print(f"  Total time: {total_time:.1f} minutes ({total_time/60:.1f} hours)")
    print(f"\n  Results saved to: {CSV_FILE}")
    print("\n  Next steps:")
    print("    1. python cleanDuplicateCSV.py")
    print("    2. cd ../Plotting && python master_plotter.py")
    print("=" * 70)
